public class Person {
    double height;
    int age;
    int weight;

    public Person()
    {
        this.height = 5.8;
        this.age = 33;
        this.weight = 140;
    }
    public Person(double height, int age, int weight)
    {
        this.height = height;
        this.age = age;
        this.weight = weight;
    }
}
